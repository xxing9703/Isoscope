[s1,~]=size(c);
if s1>3
   cmap=c;
end

n=256;
n_section=floor(n/(s1-1));
for i=1:s1-1
   for j=1:n_section
     for k=1:3
       cmap((i-1)*n_section+j,k)=c(i,k)+(c(i+1,k)-c(i,k))/n_section*(j-1);
     end
   end
end